//6.	Write a program to add 2 and 3 and print the result

public class AddTwoNumbers {
    public static void main(String[] args) {
        int num1 = 2;
        int num2 = 3;
        int sum = num1 + num2;
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}